#import "UIColor+Raindrop.h"

@implementation UIColor (Raindrop)

// Main background color
+ (UIColor *)raindropWhiteGreyBgColor {
    return RGBACOLOR(241, 242, 243, 1.0);
}

// Main blue color
+ (UIColor *)raindropBlueColor {
    return RGBACOLOR(0, 188, 212, 1.0);
}

// Main font black color
+ (UIColor *)raindropBlackFontColor {
    return RGBACOLOR(44, 44, 44, 1.0);
}

// Main font grey color
+ (UIColor *)raindropGreyFontColor {
    return RGBACOLOR(88, 88, 88, 1.0);
}
@end
